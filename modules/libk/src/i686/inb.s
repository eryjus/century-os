global          inb

;;
;; -- inb() -- get a byte from an I/O port
;;    ------------------------------------
inb:
    push        ebp
    mov         ebp,esp

    mov         edx,[ebp+8]
    xor         eax,eax
    in          al,dx

    pop         ebp
    ret

