global          outb


;;
;; -- outb() -- send a byte to an I/O port
;;    ------------------------------------
outb:
    push        ebp
    mov         ebp,esp

    mov         edx,[ebp+8]
    mov         eax,[ebp+12]
    out         dx,al

    pop         ebp
    ret
